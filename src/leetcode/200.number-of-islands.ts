// @lc code=start
function numIslands(grid: string[][]): number {
  const m = grid.length;
  const n = grid[0].length;

  const dfs = (i: number, j: number) => {
    if (i < 0 || j < 0 || i >= m || j >= n || grid[i][j] !== '1') {
      return;
    }
    grid[i][j] = '2';
    dfs(i - 1, j);
    dfs(i + 1, j);
    dfs(i, j - 1);
    dfs(i, j + 1);
  };

  let ans = 0;
  for (let i = 0; i < m; i++) {
    for (let j = 0; j < n; j++) {
      if (grid[i][j] === '1') {
        // 找到了一个新的岛
        dfs(i, j); // 把这个岛插满旗子，这样后面遍历到的 '1' 一定是新的岛
        ans++;
      }
    }
  }
  return ans;
}
